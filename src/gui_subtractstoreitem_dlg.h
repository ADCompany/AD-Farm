////////////////////////////////////////////////////////////////////////////////

// Clear
inline void CSubtractStoreItem::Clear()
{
	m_nAvailableCount = 0;
	ui.sbxSubtractCount->setValue( 0 );
}

// SetAvailableCount
inline void CSubtractStoreItem::SetAvailableCount( int nCount )
{
	if (nCount > 0)
	{
		m_nAvailableCount = nCount;
		ui.sbxSubtractCount->setMaximum( m_nAvailableCount );
	}
}

// SetAvailableCount
inline int CSubtractStoreItem::GetSutractionCount() const
{
	FM_ASSERT( ui.sbxSubtractCount );
	return ui.sbxSubtractCount->value();
}

