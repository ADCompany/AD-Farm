// setLabelColor
void CAddStoreDlg::SetLabelColor( QLabel* pLabel, bool bValid )
{
	if (!pLabel)
		return;

	QPalette palette = pLabel->palette();
	palette.setColor(pLabel->foregroundRole(), bValid ? Qt::black : Qt::red);
	pLabel->setPalette(palette);
}

